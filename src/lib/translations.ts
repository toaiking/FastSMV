// Build a normalized lookup table at runtime to prevent NFC/NFD mismatch for Vietnamese Unicode characters.
const normalizedTranslations: Record<string, string> = {};
Object.keys(translations).forEach(key => {
  normalizedTranslations[key.normalize('NFC')] = translations[key];
});

/**
 * Clean helper function to translate a string using Vietnamese as the key.
 * If language is 'vi' or string is not found, returns the string as is.
 */
export function getTranslation(text: string, language: "vi" | "en"): string {
  if (language === "vi") return text;
  
  // Look up in the dictionary using normalized key
  const trimmedNormalized = text.trim().normalize('NFC');
  const directMatch = normalizedTranslations[trimmedNormalized];
  if (directMatch) return directMatch;

  // Fallback to direct lookup just in case
  const directMatchOriginal = translations[text.trim()];
  if (directMatchOriginal) return directMatchOriginal;

  return text;
}
